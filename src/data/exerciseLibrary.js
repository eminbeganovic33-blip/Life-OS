// Helper to get exercise by ID
export function getExerciseById(id) {
  return EXERCISE_LIBRARY.find((e) => e.id === id);
}

// Helper to get exercises by muscle group
export function getExercisesByMuscle(muscleId) {
  return EXERCISE_LIBRARY.filter((e) => e.muscle === muscleId);
}

// Helper to get exercises by equipment
export function getExercisesByEquipment(equipmentId) {
  if (equipmentId === "none") {
    return EXERCISE_LIBRARY.filter((e) => e.equipment.includes("none") || e.equipment.length === 0);
  }
  return EXERCISE_LIBRARY.filter((e) => e.equipment.includes(equipmentId));
}
